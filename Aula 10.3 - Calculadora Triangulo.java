    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int a = Integer.parseInt(infoA1.getText());
        int b = Integer.parseInt(infoA2.getText());
        int c = Integer.parseInt(infoA3.getText());
        infoT1.setVisible(true);
        infoT2.setVisible(true);
       
        
        if (a<b+c && b<a+c && c<a+b) {
            infoT1.setText("Formam um triangulo");
            if (a==b && b==c){
                infoT2.setText("Equilatero");                
            } else if (a!=b && b!=c && a!=c) {
                    infoT2.setText("Escaleno");
            } else {
                    infoT2.setText("Isosceles");
            }
        } else {
                infoT1.setText("NÃ£o forma um tirangulo");
                infoT2.setText("----------------------");
                
            
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

